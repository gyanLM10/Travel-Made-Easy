import streamlit as st
import requests
import datetime

BASE_URL = "http://localhost:8080"

st.set_page_config(
    page_title="ğŸŒ Travel Made Easy",
    page_icon="ğŸŒ",
    layout="centered",
    initial_sidebar_state="expanded",
)

st.title("ğŸŒ Travel Made Easy")

if "messages" not in st.session_state:
    st.session_state.messages = []

st.header("How can I help you in planning a trip? Let me know where you want to visit.")

# Chat input box
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("User Input", placeholder=" Please give your desired location and duration")
    submit_button = st.form_submit_button("Send")

if submit_button and user_input.strip():
    response = None
    try:
        with st.spinner("Bot is thinking..."):
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload, timeout=10)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned.")
            markdown_content = f"""# ğŸŒ AI Travel Plan

**Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}  
**Created by:** Gyan's Travel Agent

---

{answer}

---

*This travel plan was generated by AI. Please verify all information, especially prices, operating hours, and travel requirements before your trip.*
"""
            st.markdown(markdown_content)
        else:
            st.error(f"Bot failed to respond: {response.text}")

    except Exception as e:
        st.error(f"Something went wrong: {e}")

